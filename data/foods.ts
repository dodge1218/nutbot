/**
 * Helper to find food by ID
 */
export function getFoodById(id: string): FoodItem | undefined {
  return FOODS_DATABASE.find(f => f.id === id);
}

/**
 * Helper to search foods by name
 */
export function searchFoods(query: string): FoodItem[] {
  const lowerQuery = query.toLowerCase();
  return FOODS_DATABASE.filter(f => 
    f.name.toLowerCase().includes(lowerQuery) ||
    f.category?.toLowerCase().includes(lowerQuery)
  );
}

/**
 * Get foods by category
 */
export function getFoodsByCategory(category: string): FoodItem[] {
  return FOODS_DATABASE.filter(f => f.category === category);
}

/**
 * Get all food categories
 */
export function getCategories(): string[] {
  const categories = new Set(FOODS_DATABASE.map(f => f.category).filter(Boolean));
  return Array.from(categories) as string[];
}
